package br.uniriotec.oscip.beans;

public class EmpenhoConv {
	
	String id;
	String idConvenio;
	String tipoNota;
	String descTipoNota;
	String dtEmissao;
	String codSituacao;
	String descSituacao;
	String valor;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIdConvenio() {
		return idConvenio;
	}
	public void setIdConvenio(String idConvenio) {
		this.idConvenio = idConvenio;
	}
	public String getTipoNota() {
		return tipoNota;
	}
	public void setTipoNota(String tipoNota) {
		this.tipoNota = tipoNota;
	}
	public String getDescTipoNota() {
		return descTipoNota;
	}
	public void setDescTipoNota(String descTipoNota) {
		this.descTipoNota = descTipoNota;
	}
	public String getDtEmissao() {
		return dtEmissao;
	}
	public void setDtEmissao(String dtEmissao) {
		this.dtEmissao = dtEmissao;
	}
	public String getCodSituacao() {
		return codSituacao;
	}
	public void setCodSituacao(String codSituacao) {
		this.codSituacao = codSituacao;
	}
	public String getDescSituacao() {
		return descSituacao;
	}
	public void setDescSituacao(String descSituacao) {
		this.descSituacao = descSituacao;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	
}
